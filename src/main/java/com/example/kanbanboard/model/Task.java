package com.example.kanbanboard.model;

import java.time.LocalDateTime;

public class Task {

    private String id;
    private String title;
    private String description;
    private TaskStatus status = TaskStatus.TODO; // default leftmost column
    private LocalDateTime createdAt = LocalDateTime.now();

    // when admin assigned the task
    private LocalDateTime assignedAt = LocalDateTime.now();

    // deadline chosen by admin
    private LocalDateTime deadline;

    // still stored if you want to keep physical column mapping
    private String columnId;

    public Task() {
    }

    public String getColumnId() {
        return columnId;
    }

    public void setColumnId(String columnId) {
        this.columnId = columnId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public TaskStatus getStatus() {
        return status;
    }

    public void setStatus(TaskStatus status) {
        this.status = status;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getAssignedAt() {
        return assignedAt;
    }

    public void setAssignedAt(LocalDateTime assignedAt) {
        this.assignedAt = assignedAt;
    }

    public LocalDateTime getDeadline() {
        return deadline;
    }

    public void setDeadline(LocalDateTime deadline) {
        this.deadline = deadline;
    }
}
